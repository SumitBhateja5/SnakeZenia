const food = new SnakeNode(random_position(), "yellow");
food.draw();

const init = setInterval(() => {
    ll.head.clear();
    let oldPos = ll.head.position;
    ll.head.position = [
        ll.head.position[0] + ll.direction[0],
        ll.head.position[1] + ll.direction[1],
    ];

    if (
        ll.head.position[0] < 0 ||
        ll.head.position[0] >= 30 ||
        ll.head.position[1] < 0 ||
        ll.head.position[1] >= 30
    ) {
        clearInterval(init);
    } else {
        ll.head.draw();
        let temp = ll.head.next;
        while (temp) {
            temp.clear();
            let old = temp.position;
            temp.position = oldPos;
            oldPos = old;
            temp.draw();
            temp = temp.next;
        }

        if (
            ll.head.position[0] == food.position[0] &&
            ll.head.position[1] == food.position[1]
        ) {
            food.position = random_position();
            food.draw();
            ll.push();
        }
    }
}, 100);

window.addEventListener("keypress", (event) => {
    if (event.key == "w" || event.key == "W") {
        if (ll.direction[0] == 1) {
            ll.reverse();
        }
        ll.direction = [-1, 0];
    } else if (event.key == "a" || event.key == "A") {
        if (ll.direction[1] == 1) {
            ll.reverse();
        }

        ll.direction = [0, -1];
    } else if (event.key == "d" || event.key == "D") {
        if (ll.direction[1] == -1) {
            ll.reverse();
        }
        ll.direction = [0, 1];
    } else if (event.key == "s" || event.key == "S") {
        if (ll.direction[0] == -1) {
            ll.reverse();
        }
        ll.direction = [1, 0];
    }
});
