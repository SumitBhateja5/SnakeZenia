class SnakeNode {
    position: number[];
    next?: SnakeNode;
    fill: string;
    constructor(pos: number[], fill = "green") {
        this.position = pos;
        this.next = undefined;
        this.fill = fill;
    }

    draw() {
        const current = game_rows[this.position[0]];
        const current_col = current.children[
            this.position[1]
        ] as HTMLDivElement;
        current_col.style.background = this.fill;
    }

    clear() {
        const current = game_rows[this.position[0]];
        const current_col = current.children[
            this.position[1]
        ] as HTMLDivElement;
        current_col.style.background = "transparent";
    }
}

class LinkedList {
    head: SnakeNode;
    direction: number[];

    constructor(head: SnakeNode) {
        this.head = head;
        this.direction = get_random_dirn();
    }

    draw() {
        let temp = this.head;
        while (temp) {
            temp.draw();
            temp = temp.next;
        }
    }
    push() {
        let temp = this.head;
        while (temp.next) {
            temp = temp.next;
        }

        let newNode = new SnakeNode([
            temp.position[0] - ll.direction[0],
            temp.position[1] - ll.direction[1],
        ]);
        temp.next = newNode;
    }
    reverse() {
        let temp = this.head;
        if (this.head.next == undefined) {
            return;
        }
        let next,
            prev: SnakeNode = undefined;

        while (temp) {
            next = temp.next;
            temp.next = prev;
            prev = temp;
            temp = next;
        }

        this.head = prev;
    }
    print() {
        let temp = this.head;
        while (temp) {
            console.log(temp.position);
            temp = temp.next;
        }
    }
}

